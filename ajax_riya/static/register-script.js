$(document).ready(function(){
    $("#btnReg").click(function(){
        $myname=$("#txt1").val();
        $myaddress=$("#txt2").val();
        //alert("gh");
        $.ajax({
            url:'regcod',
            data:{'name':$myname,'address':$myaddress},
            type:'get',
            dataType:'json',
            success:function(d){
                if(d.sts)
                {
                    alert(d.msg);
                }
                else{
                    alert(d.msg);
                }
                
            },error:function(d1)
            {
                console.log(d1);
            }
        });
    });
});